# Initializing a list of integers
integers = [1, 2, 3, 4]
print(integers)  # Output: [1, 2, 3, 4]

# Initializing a list of strings
stringList = ['Milk', 'Cheese', 'Butter']
print(stringList)  # Output: ['Milk', 'Cheese', 'Butter']

# Initializing a list with mixed data types
mixedList = [1, 1.5, 'spam']
print(mixedList)  # Output: [1, 1.5, 'spam']

# Initializing a nested list (a list within a list)
nestedList = [1, 2, 3, 4, 5, [1.5, 1.6], ['test']]
print(nestedList)  # Output: [1, 2, 3, 4, 5, [1.5, 1.6], ['test']]

# Initializing an empty list
empty = []
print(empty)  # Output: []
