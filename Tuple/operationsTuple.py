tup1 = (1,4,3,2,5)  # Create a tuple named 'tup1' with elements 1, 4, 3, 2, and 5
tup2 = (1,2,6,9,8,7)  # Create a tuple named 'tup2' with elements 1, 2, 6, 9, 8, and 7

print(tup1 + tup2)  # Concatenate tuples 'tup1' and 'tup2' and print the result

print(tup1 * 4)  # Repeat tuple 'tup1' four times and print the result

print(tup1.count(2))  # Count the number of occurrences of '2' in 'tup1' and print it

print(tup1.index(4))  # Find the index of the first occurrence of '4' in 'tup1' and print it

print(len(tup1))  # Get the length (number of elements) of 'tup1' and print it

print(max(tup1))  # Find the maximum value in 'tup1' and print it

print(min(tup1))  # Find the minimum value in 'tup1' and print it

