# Creating an empty dictionary using the dict() constructor
a = dict()
print(a)  # Output: {}

# Creating an empty dictionary using the literal syntax {}
b = {}
print(b)  # Output: {}

# Creating a dictionary using the dict() constructor with keyword arguments
c = dict(key1="Value1", key2="Value2", key3="Value3")
print(c)  # Output: {'key1': 'Value1', 'key2': 'Value2', 'key3': 'Value3'}

# Creating a dictionary with predefined key-value pairs using literal syntax
d = {"one": "Value1", "two": "Value2", "three": "Value3"}
print(d)  # Output: {'one': 'Value1', 'two': 'Value2', 'three': 'Value3'}
