myList = ['a', 'b', 'c', 'd', 'e', 'f']

# Print a slice from index 0 to 2 (not including 2)
print(myList[0:2])  # Output: ['a', 'b']

# Print the entire list (from the start to the end)
print(myList[:])  # Output: ['a', 'b', 'c', 'd', 'e', 'f']

# Print the list excluding the last element
print(myList[:-1])  # Output: ['a', 'b', 'c', 'd', 'e']

# Print the last two elements of the list
print(myList[-2:])  # Output: ['e', 'f']
